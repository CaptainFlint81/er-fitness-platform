export function getExerciseCards() {
  return exerciseLibrary.map((exercise) => ({
    title: exercise.name,
    description: `${exercise.teachingFocus} Setup: ${exercise.setup}`,
    meta: `${exercise.system} | ${exercise.level}`
  }));
}

export function getRoutineCards() {
  return routineLibrary.map((routine) => ({
    title: routine.title,
    description: routine.goal,
    href: `/routines#${routine.slug}`,
    meta: `${routine.system} | ${routine.duration}`
  }));
}
